CREATE DATABASE IF NOT EXISTS movie_sq DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci;

CREATE USER IF NOT EXISTS 'root'@'%' IDENTIFIED BY '1234';

CREATE USER IF NOT EXISTS 'admin0'@'%' IDENTIFIED BY '1234';

CREATE USER IF NOT EXISTS 'admin0'@'localhost' IDENTIFIED BY '1234';

GRANT ALL PRIVILEGES ON *.* TO 'root'@'%' WITH GRANT OPTION;

GRANT ALL PRIVILEGES ON *.* TO 'root'@'localhost' WITH GRANT OPTION;

GRANT ALL PRIVILEGES ON *.* TO 'admin0'@'%' WITH GRANT OPTION;

GRANT ALL PRIVILEGES ON *.* TO 'admin0'@'localhost' WITH GRANT OPTION;

FLUSH PRIVILEGES;

USE movie_sq;
